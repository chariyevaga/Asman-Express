CREATE OR ALTER FUNCTION [dbo].[AGO_TIMETOINT](@HH INT,@MM INT,@SS INT)
       RETURNS INT
AS
BEGIN
 DECLARE @TIME    INT
 SELECT @TIME   = (@HH*65536*256+@MM*65536+@SS*256)
 RETURN(@TIME)
END
